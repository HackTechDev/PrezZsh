                    <h3>Z Shell == zsh</h3>
                    <p>
* C'est un Shell Unix<br/>
* Shell : Couche logicielle qui fournit une interface pour l'utilisateur d'un système d'exploitation<br/>
* Unix  : Une famille de système d'exploitation<br/>
* Son utilisation est interactive<br/>
* Autres Shell Unix : bash, ksh, tcsh<br/>
* Créateur : Paul Falstad<br/>
* Date de création  : 1990 <br/>
* Lieu de création : Université de Princeton<br/>
* Z =  Zhong Shao, professeur à Yale<br/>
