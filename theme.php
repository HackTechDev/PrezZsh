                    <h3>Activation du theme</h3>
		    <p>
* Ouvrir :<br/> 
<pre>
~/.zshrc
</pre>
<br/>
* Chercher :<br/>
<pre>
ZSH_THEME="robbyrussell"
</pre>
* Remplacer par : <br>
<pre>
ZSH_THEME="powerlevel9k/powerlevel9k"
</pre>
* Recharcharger le fichier de configuration:<br/>
<pre>
$ source .~/.zshrc
</pre>
                    </p>
                
