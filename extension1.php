                    <h3>Activation des extensions</h3>
		    <p>
* Ouvrir :<br/> 
<pre>
~/.zshrc
</pre>
<br/>
* Chercher :<br/>
<pre>
plugins=(
  git
)
</pre>
* Remplacer par : <br>
<pre>
plugins=(
  git
  screen
  sudo
  tig
  ubuntu
  history
  gnu-utils
  zsh-syntax-highlighting 
  zsh-autosuggestions
  colored-man-colored
)
</pre>
* Recharger le fichier de configuration:<br/>
<pre>
$ source .~/.zshrc
</pre>
                    </p>
                
