                    <h3>Installation</h3>
		    <p>
* Installation :<br/> 
<pre>
$ sudo apt-get install zsh
</pre>
<br/>
* Vérification :<br/>
<pre>
$ zsh --version
zsh 5.4.2 (x86_64-ubuntu-linux-gnu)
</pre>
* Rendre zsh comme shell par défaut :<br/>
<pre>
$ chsh -s $(which zsh)
</pre>
* Se déconnecter et se reconnecter.<br/>
                    </p>
                
